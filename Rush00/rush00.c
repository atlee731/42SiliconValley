void	ft_putchar(char c);

void	rush(int w, int h)
{
	int x = 0 ;
	int y = 0 ;

	while (y < h)
	{
 		while (x < w)
		{
		if ((x == 0 && y == 0)||(x == 0 && y == h-1)||(x == w-1 && y == 0)||(x == w-1 && y == h-1))
		{
			ft_putchar('o');
		}
		else if (y == 0 || y == h-1)
		{
			ft_putchar('-');
		}
		else if (x == 0 || x == w-1)
		{
			ft_putchar('|');
		}
		else
		{
			ft_putchar(' ');
		}
		x++;
	}
	ft_putchar('\n');
	y++;
	x = 0;
	}
}
